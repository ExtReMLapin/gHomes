ghomes.languages.en  = 2;
ghomes.languages.ru  = 3;


function ghomes.picklanguage(_language)
	if not _language then _language = ghomes.languages.en end
	if not ghomes.languages.table[_language] then _language = ghomes.languages.en  end
	table.Merge(ghomes,ghomes.languages.table[_language])
	ghomes.languages.currentLang = _language
end


